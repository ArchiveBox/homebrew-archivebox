#!/usr/bin/env python3
"""Build Formula/archivebox.rb from the latest ArchiveBox dev commit."""

from __future__ import annotations

import argparse
import datetime as dt
import os
import shutil
import subprocess
import sys
import tempfile
import textwrap
import tomllib
from pathlib import Path


REPO_ROOT = Path(__file__).resolve().parents[1]
FORMULA_PATH = REPO_ROOT / "Formula" / "archivebox.rb"
ARCHIVEBOX_REPO = "https://github.com/ArchiveBox/ArchiveBox.git"


def run(*args: str, cwd: Path | None = None) -> str:
    return subprocess.check_output(args, cwd=cwd, text=True).strip()


def github_output(**values: str) -> None:
    output_path = os.environ.get("GITHUB_OUTPUT")
    if not output_path:
        return
    with open(output_path, "a", encoding="utf-8") as output:
        for key, value in values.items():
            output.write(f"{key}={value}\n")


def clone_archivebox_dev() -> tuple[Path, tempfile.TemporaryDirectory[str]]:
    tmpdir = tempfile.TemporaryDirectory(prefix="archivebox-dev-")
    checkout = Path(tmpdir.name) / "ArchiveBox"
    run(
        "git",
        "clone",
        "--depth=1",
        "--branch=dev",
        "--filter=blob:none",
        ARCHIVEBOX_REPO,
        str(checkout),
    )
    return checkout, tmpdir


def formula_template(version: str, commit: str, commit_epoch: int) -> str:
    commit_time = dt.datetime.fromtimestamp(commit_epoch, tz=dt.UTC)
    formula_version = f"{version}.{commit_time:%Y%m%d%H%M%S}"

    return textwrap.dedent(
        f"""\
        # Auto-generated by bin/build_brew.sh.
        # Tracks ArchiveBox/ArchiveBox@dev without vendoring Python dependency resources.

        class Archivebox < Formula
          desc "Self-hosted internet archiving solution"
          homepage "https://archivebox.io"
          url "https://github.com/ArchiveBox/ArchiveBox.git",
              revision: "{commit}"
          version "{formula_version}"
          license "MIT"
          head "https://github.com/ArchiveBox/ArchiveBox.git", branch: "dev"

          depends_on "python@3.13"

          def install
            venv = libexec/"venv"
            python = Formula["python@3.13"].opt_bin/"python3.13"
            archivebox_revision = stable.specs.fetch(:revision)

            system python, "-m", "venv", venv
            system venv/"bin/python", "-m", "pip", "install", "--upgrade", "pip", "setuptools", "wheel"
            (buildpath/"homebrew-constraints.txt").write("cbor2<6\\n")
            system venv/"bin/python", "-m", "pip", "install", "--constraint", buildpath/"homebrew-constraints.txt", "."

            site_packages_cmd = "#{{venv}}/bin/python -c 'import site; print(site.getsitepackages()[0])'"
            site_packages = Pathname(shell_output(site_packages_cmd).strip)
            (site_packages/".git/refs/heads").mkpath
            (site_packages/".git/HEAD").write("ref: refs/heads/dev\\n")
            (site_packages/".git/refs/heads/dev").write("#{{archivebox_revision}}\\n")

            bin.install_symlink venv/"bin/archivebox"
          end

          def caveats
            <<~EOS
              ArchiveBox is installed from ArchiveBox/ArchiveBox@dev:
                #{{stable.specs.fetch(:revision)}}

              To create a collection and install runtime extractors:
                mkdir -p ~/archivebox/data
                cd ~/archivebox/data
                archivebox init
                archivebox install
            EOS
          end

          test do
            assert_match "{version}", shell_output("#{{bin}}/archivebox version")
          end
        end
        """
    )


def update_formula(check: bool) -> int:
    checkout, tmpdir = clone_archivebox_dev()
    try:
        pyproject = tomllib.loads((checkout / "pyproject.toml").read_text(encoding="utf-8"))
        version = pyproject["project"]["version"]
        commit = run("git", "rev-parse", "HEAD", cwd=checkout)
        commit_epoch = int(run("git", "show", "-s", "--format=%ct", "HEAD", cwd=checkout))
        short_commit = commit[:12]
        commit_time = dt.datetime.fromtimestamp(commit_epoch, tz=dt.UTC)
        formula_version = f"{version}.{commit_time:%Y%m%d%H%M%S}"
        new_formula = formula_template(version, commit, commit_epoch)
    finally:
        tmpdir.cleanup()

    old_formula = FORMULA_PATH.read_text(encoding="utf-8") if FORMULA_PATH.exists() else ""
    changed = old_formula != new_formula

    github_output(
        changed=str(changed).lower(),
        archivebox_version=version,
        archivebox_commit=commit,
        archivebox_short_commit=short_commit,
        formula_version=formula_version,
    )

    if check and changed:
        print(f"{FORMULA_PATH} is not up to date", file=sys.stderr)
        return 1

    if changed:
        FORMULA_PATH.parent.mkdir(parents=True, exist_ok=True)
        FORMULA_PATH.write_text(new_formula, encoding="utf-8")
        print(f"Updated {FORMULA_PATH.relative_to(REPO_ROOT)} to {formula_version} ({short_commit})")
    else:
        print(f"{FORMULA_PATH.relative_to(REPO_ROOT)} already tracks {formula_version} ({short_commit})")

    return 0


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--check", action="store_true", help="fail if Formula/archivebox.rb is stale")
    args = parser.parse_args()

    if not shutil.which("git"):
        print("git is required", file=sys.stderr)
        return 127

    return update_formula(check=args.check)


if __name__ == "__main__":
    raise SystemExit(main())
